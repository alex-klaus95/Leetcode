
public class Solution2769 {
    public static int theMaximumAchievableX(int num, int t) {
    	
       int x = num + t + t;
    	
    	
    	return x;
    }
    
    
    
    public static void main(String[] args) {
    	
    	int num = 4, t = 1;
    	System.out.println(theMaximumAchievableX(num, t));
    }
    
}
